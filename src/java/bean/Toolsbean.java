public class Toolsbean {
    int id;
    String name, brand, img, tools_desc;
    float rent_price, buy_price;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getTools_desc() {
        return tools_desc;
    }

    public void setTools_desc(String tools_desc) {
        this.tools_desc = tools_desc;
    }

    public float getRent_price() {
        return rent_price;
    }

    public void setRent_price(float rent_price) {
        this.rent_price = rent_price;
    }

    public float getBuy_price() {
        return buy_price;
    }

    public void setBuy_price(float buy_price) {
        this.buy_price = buy_price;
    }
    
    
}
